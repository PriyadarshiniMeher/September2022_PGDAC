class P17{
	public static void main(String args[]){
		int count=1;
		for(int i =1; i<=5;i++){               //row
			for(int k =1; k<=i; k++){         //col
			
				System.out.print(count);
				count++;
				System.out.print(" ");
	
			}
			
			System.out.println();
		
		}
	
	
	
	
	
	}

}